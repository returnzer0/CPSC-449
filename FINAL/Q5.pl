/*
** Is there a round from C1 to C2 departing at least SoonestAllowed minutes
** in the future?  DepartureTime, ArrivalTime and Route will all be 
** uninstantiated variables when routeAfter is called.  These variables
** will be instantiated with the number of minutes in the future that the
** flight actually departs, the number of minutes in the future when the
** flight actually arrives, and a list containing a fly structure 
** that describes flying from C1 to C2.
*/
routeAfter(C1, C2, SoonestAllowed, DepartureTime, ArrivalTime, Route) :- 
  flight(C1, C2, DepartureTime, Duration),
  DepartureTime >= SoonestAllowed,
  ArrivalTime is DepartureTime + Duration,
  Route = [fly(C1, C2)].

